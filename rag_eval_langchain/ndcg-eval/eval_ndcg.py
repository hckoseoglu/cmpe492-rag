"""
Retrieval-only evaluation using nDCG@10.

Loads a corpus (gold + distractors + partly-relevant docs) into a vector
store, retrieves for each query, and computes nDCG / precision / recall / MRR
against the gold relevance judgments (qrels).

Supports two data sources (set DATA_SOURCE below):
  - "local"  : uses data/ generated by build_dataset.py in this folder
  - "mteb"   : uses ../../mteb/data/ from the MTEB benchmark
"""

import json
import math
import os
import sys

# Allow imports from parent rag_eval_langchain directory
sys.path.insert(0, os.path.join(os.path.dirname(__file__), ".."))

from langchain_core.documents import Document
from langchain_core.vectorstores import InMemoryVectorStore
from langchain_openai import OpenAIEmbeddings

# ── Config ──────────────────────────────────────────────────────────────────

EMBEDDING_MODEL = "text-embedding-3-large"
RETRIEVAL_K = 10

# Reranker
RERANKER = True
RERANKER_MODEL = "BAAI/bge-reranker-v2-m3"
RERANKER_TOP_K = 5

# Data source: "local" (ndcg-eval/data/) or "mteb" (mteb/data/)
DATA_SOURCE = "local"

_SCRIPT_DIR = os.path.dirname(__file__)
_DATA_DIRS = {
    "local": os.path.join(_SCRIPT_DIR, "data"),
    "mteb": os.path.join(_SCRIPT_DIR, "..", "..", "mteb", "data"),
}
_DATA_DIR = _DATA_DIRS[DATA_SOURCE]
CORPUS_PATH = os.path.join(_DATA_DIR, "corpus.json")
QUERIES_PATH = os.path.join(_DATA_DIR, "queries.json")
QRELS_PATH = os.path.join(_DATA_DIR, "qrels.json")


# ── Metrics ─────────────────────────────────────────────────────────────────


def _dcg(relevances: list[float], k: int) -> float:
    """Discounted Cumulative Gain at k."""
    score = 0.0
    for i, rel in enumerate(relevances[:k]):
        score += rel / math.log2(i + 2)  # i+2 because log2(1)=0
    return score


def ndcg_at_k(relevances: list[float], k: int) -> float:
    """Normalized DCG at k."""
    dcg = _dcg(relevances, k)
    ideal = _dcg(sorted(relevances, reverse=True), k)
    if ideal == 0:
        return 0.0
    return dcg / ideal


def precision_at_k(relevances: list[float], k: int) -> float:
    """Fraction of top-k results that are relevant."""
    top_k = relevances[:k]
    return sum(1 for r in top_k if r > 0) / k


def recall_at_k(relevances: list[float], k: int, total_relevant: int) -> float:
    """Fraction of relevant docs found in top-k."""
    if total_relevant == 0:
        return 0.0
    top_k = relevances[:k]
    return sum(1 for r in top_k if r > 0) / total_relevant


def reciprocal_rank(relevances: list[float]) -> float:
    """Reciprocal rank of the first relevant result."""
    for i, rel in enumerate(relevances):
        if rel > 0:
            return 1.0 / (i + 1)
    return 0.0


# ── Main ────────────────────────────────────────────────────────────────────


def main():
    # ── 1. Load & index corpus ──────────────────────────────────────────────
    print(f"[1/4] Loading and indexing corpus from {DATA_SOURCE} data...")
    with open(CORPUS_PATH) as f:
        corpus_raw = json.load(f)

    documents = []
    for doc in corpus_raw:
        documents.append(
            Document(
                page_content=doc["text"],
                metadata={"id": doc["id"], "title": doc["title"]},
            )
        )

    embeddings = OpenAIEmbeddings(model=EMBEDDING_MODEL)
    vector_store = InMemoryVectorStore(embeddings)
    vector_store.add_documents(documents)
    print(f"  Indexed {len(documents)} documents")

    # ── 2. Load queries & qrels ─────────────────────────────────────────────
    print("[2/4] Loading queries and relevance judgments...")
    with open(QUERIES_PATH) as f:
        queries_raw = json.load(f)
    with open(QRELS_PATH) as f:
        qrels_raw = json.load(f)

    # Build lookup: {query_id: {doc_id: score}}
    qrels_lookup: dict[str, dict[str, int]] = {}
    for qrel in qrels_raw:
        qid = qrel["query-id"]
        did = qrel["corpus-id"]
        score = qrel["score"]
        qrels_lookup.setdefault(qid, {})[did] = score

    print(f"  {len(queries_raw)} queries, {len(qrels_raw)} relevance judgments")

    # ── 3. Retrieve & optionally rerank ─────────────────────────────────────
    print("[3/4] Retrieving documents for each query...")

    if RERANKER:
        from reranking import load_reranker, rerank_docs

        reranker_model = load_reranker(RERANKER_MODEL)
        print(f"  Reranker loaded: {RERANKER_MODEL}")
        eval_k = RERANKER_TOP_K
    else:
        reranker_model = None
        eval_k = RETRIEVAL_K

    all_relevances: list[list[float]] = []
    total_relevant_per_query: list[int] = []

    for query in queries_raw:
        qid = query["id"]
        q_text = query["text"]
        gold = qrels_lookup.get(qid, {})

        # Retrieve more than eval_k if reranking (reranker selects from a larger pool)
        fetch_k = RETRIEVAL_K if RERANKER else eval_k
        results = vector_store.similarity_search(q_text, k=fetch_k)

        if RERANKER:
            results = rerank_docs(
                q_text, results, top_k=RERANKER_TOP_K, model=reranker_model
            )

        # Build relevance vector for the ranked results
        relevances = [float(gold.get(doc.metadata["id"], 0)) for doc in results]
        all_relevances.append(relevances)
        total_relevant_per_query.append(sum(1 for s in gold.values() if s > 0))

    # ── 4. Compute metrics ──────────────────────────────────────────────────
    print("[4/4] Computing metrics...")

    metrics: dict[str, float] = {}

    for k in [1, 3, 5]:
        ndcg_scores = [ndcg_at_k(rels, k) for rels in all_relevances]
        metrics[f"nDCG@{k}"] = sum(ndcg_scores) / len(ndcg_scores)

    for k in [3, 5]:
        prec_scores = [precision_at_k(rels, k) for rels in all_relevances]
        recall_scores = [
            recall_at_k(rels, k, tr)
            for rels, tr in zip(all_relevances, total_relevant_per_query)
        ]
        metrics[f"Precision@{k}"] = sum(prec_scores) / len(prec_scores)
        metrics[f"Recall@{k}"] = sum(recall_scores) / len(recall_scores)

    mrr_scores = [reciprocal_rank(rels) for rels in all_relevances]
    metrics["MRR"] = sum(mrr_scores) / len(mrr_scores)

    # ── Print results ───────────────────────────────────────────────────────
    print("\n" + "=" * 50)
    print("  Retrieval Evaluation Results")
    print("=" * 50)
    print(f"  Data source     : {DATA_SOURCE}")
    print(f"  Corpus size     : {len(documents)}")
    print(f"  Embedding model : {EMBEDDING_MODEL}")
    print(f"  Retrieval K     : {RETRIEVAL_K}")
    if RERANKER:
        print(f"  Reranker        : {RERANKER_MODEL}")
        print(f"  Reranker top-K  : {RERANKER_TOP_K}")
    print("-" * 50)
    for name, value in metrics.items():
        print(f"  {name:<15} {value:.4f}")
    print("=" * 50)


if __name__ == "__main__":
    main()
